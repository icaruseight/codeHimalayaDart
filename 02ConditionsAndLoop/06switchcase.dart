void main() {
  int week = 8;
  switch (week) {
    case 1:
      print("It is Sunday today.");
      break;
    case 2:
      print(" It is Monday today.");
      break;
    case 3:
      print("It is Tuesday today.");
      break;
    case 4:
      print("It is Wednesday today");
      break;
    case 5:
      print("It is Thursday today");
      break;
    case 6:
      print("It is Friday today");
      break;
    case 7:
      print("It is Saturday today");
      break;
    default:
      print(
          "Please choose from 1-7 as there are only 7 days in a week. Thank you.");
      break;
  }
}
